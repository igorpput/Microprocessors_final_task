/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* MCU Configuration--------------------------------------------------------*/
  HAL_Init();
  SystemClock_Config();

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_ETH_Init();
  MX_I2C1_Init();
  MX_USART3_UART_Init();
  MX_USB_OTG_FS_PCD_Init();
  MX_TIM1_Init();

  /* USER CODE BEGIN 2 */
  // Start PWM first
  PWM_LED_Start(&htim1, TIM_CHANNEL_1);

  // Visible quick test: 70% brightness for 1 second
  PWM_LED_SetDuty(&htim1, TIM_CHANNEL_1, 0.7f);
  HAL_Delay(1000);

  // Init BH1750
  BH1750_Init(&hi2c1);

  // PI init (more stable gains to start)
  // Ts = 0.2s (200 ms)
  PI_Init(&pi, 0.3f, 0.05f, 0.2f, 0.0f, 1.0f);

  last_ms = HAL_GetTick();

  // initial output
  PWM_LED_SetDuty(&htim1, TIM_CHANNEL_1, u_last);
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
    // run every 200ms
    if (HAL_GetTick() - last_ms >= 200U)
    {
      last_ms += 200U;

      // Try read lux
      if (BH1750_ReadLux(&hi2c1, &lux) == HAL_OK)
      {
        // sanity clamp
        if (lux < 0.0f) lux = 0.0f;
        if (lux > 100000.0f) lux = 100000.0f;

        float u = PI_Update(&pi, target_lux, lux);

        // update only on valid reading
        u_last = u;
        PWM_LED_SetDuty(&htim1, TIM_CHANNEL_1, u_last);
      }
      else
      {
        // IMPORTANT: Do NOT force duty to 0 on read fail.
        // Keep last output to avoid flicker.
        PWM_LED_SetDuty(&htim1, TIM_CHANNEL_1, u_last);
      }

      // OPTIONAL UART debug (uncomment if you want)
      /*
      int n = snprintf(uart_buf, sizeof(uart_buf),
                       "lux=%.1f target=%.1f duty=%.2f\r\n",
                       lux, target_lux, u_last);
      HAL_UART_Transmit(&huart3, (uint8_t*)uart_buf, n, 50);
      */
    }
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
  }
  /* USER CODE END 3 */
}

